class Command(BaseCommand):
    help = '給与計算用の初期マスタデータを投入します'

    def add_arguments(self, parser):
        parser.add_argument(
            '--with-employees', action='store_true',
            help='社員マスタも作成する（初回セットアップ用）')

    def handle(self, *args, **options):
        self._setup_insurance_rates()
        self._setup_tax_table()

        if options['with_employees']:
            self._setup_employees()

        self.stdout.write(self.style.SUCCESS('マスタデータの投入が完了しました'))

    def _setup_insurance_rates(self):
        """保険料率を投入"""
        for year in [2024, 2025]:
            rate, created = InsuranceRate.objects.update_or_create(
                fiscal_year=year,
                defaults={
                    'pension_rate': 18.30,
                    'health_rate': 9.98,
                    'nursing_rate': 1.60,
                    'employment_rate_employee': 0.600,
                    'employment_rate_employer': 0.950,
                    'prefecture': '東京都',
                }
            )
            status = '作成' if created else '更新'
            self.stdout.write(f'  保険料率 {year}年度: {status}')

    def _setup_tax_table(self):
        """源泉徴収税額表を投入"""
        for year in [2024, 2025]:
            # 既存データを削除
            deleted, _ = WithholdingTaxRow.objects.filter(
                fiscal_year=year).delete()
            if deleted:
                self.stdout.write(f'  源泉徴収税額表 {year}年: {deleted}行削除')

            rows = []
            for row_data in TAX_TABLE_2025:
                rows.append(WithholdingTaxRow(
                    fiscal_year=year,
                    salary_from=row_data[0],
                    salary_to=row_data[1],
                    tax_dep_0=row_data[2],
                    tax_dep_1=row_data[3],
                    tax_dep_2=row_data[4],
                    tax_dep_3=row_data[5],
                    tax_dep_4=row_data[6],
                    tax_dep_5=row_data[7],
                    tax_dep_6=row_data[8],
                    tax_dep_7=row_data[9],
                ))
            WithholdingTaxRow.objects.bulk_create(rows)
            self.stdout.write(f'  源泉徴収税額表 {year}年: {len(rows)}行投入')

